package com.onysakura.algorithm.core;

public class SwitchOrder {

    public static void main(String[] args) {
        String a = "3";
        switch (a) {
            default:
                System.out.println("default");
            case "1":
                System.out.println("1");
                break;
            case "2":
                System.out.println("2");
        }
    }

}
